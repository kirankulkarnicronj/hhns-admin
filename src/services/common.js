import axios from 'axios'
import serverAddress from './config'

export async function getTopicList() {
  const url = `${serverAddress}/api/topic/`
  return axios
    .get(url)
    .then(response => {
      if (response && response.data) {
        return response
      }
      return false
    })
    .catch(error => {
      return error
    })
}

